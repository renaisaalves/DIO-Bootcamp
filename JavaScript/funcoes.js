// FUNÇÕES

/* Vamos conhecer uma estrutura básica de funções em JavaScript. 
   Os parênteses em soma() servem para declarar atributos (parâmetros).
*/

function soma(a, b) {
    console.log(a + b);
    return a + b;
}

/* Reparem que temos a nossa função prontinha. Ela serve para somar dois valores, os quais serão informados.
   - O comando "return" serve para retornar o resultado da instrução que foi dada na função, mas não tem o caráter de exibir na tela esse resultado. 
   - Para imprimir na tela alguma coisa, usamos no JavaScript o comando console.log(), que tem a mesma função do print() no Python.
*/